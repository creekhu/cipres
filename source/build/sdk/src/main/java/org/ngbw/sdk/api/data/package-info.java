/**
 * API The NGBW-SDK Application Programming Interface - data
 * Package contains all Interfaces relevant for the registration,
 * access, and interrogation of external data collections.
 * DataResource - interface for physical data access to 1 or more Datasets
 * Dataset - interface for any data collection such as swissprot, genbank, pdb etc.
 * DatasetConfig - interface for a Dataset-specific DataResource configuration bean
 * DatasetService - interface for standardized access and registration of DataResources
 * MetaQuery - base interface for concurrent interrogation of multiple Datasets across multiple DatResources
 * SimpleSearchMetaQuery - interface for a simple search (single search phrase, no operators, no fields)
 * AdvancedSearchMetaQuery - interface for advanced search including search fields and operators
 * Criterion - interface to specify restrictions or logical operation (where clause) for an AdvancedSearchMetaQuery
 * DataResourceQuery - base interface for interrogation an individual Dataset hosted by a DataResource
 * SimpleQuery and AdvancedQuery are interfaces extend the DataResourceQuery to accommodate the
 * search criteria for a SimpleSearch- and AdvancedSearchMetaQuery on a per Dataset level
 * QueryResult- interface for the result returned by the execute method of a DataResourceQuery
 * ResultTupe - interface for a row (element) of the QueryResult
 * 
 */
package org.ngbw.sdk.api.data;