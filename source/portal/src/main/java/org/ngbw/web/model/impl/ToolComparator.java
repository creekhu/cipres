package org.ngbw.web.model.impl;

import java.util.Comparator;

public class ToolComparator implements Comparator<String>
{
	public int compare(String tool1, String tool2) {
		if (tool1 == null || tool2 == null)
			throw new NullPointerException("Compared tools cannot be null.");
		else return tool1.compareTo(tool2);
	}
}
